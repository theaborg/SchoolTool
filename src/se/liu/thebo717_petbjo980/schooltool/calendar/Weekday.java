/**
 * Used to represent a weekday.
 * Is used in ScheduledAppointment-objects.
 * In BaseViewer, appointments are distributed by Weekday, so they can
 * be viewed on the right day.
 */
public enum Weekday
{
    SUNDAY, MONDAY, TUESDAY, WEDNESDAY, THURSDAY, FRIDAY, SATURDAY
}
